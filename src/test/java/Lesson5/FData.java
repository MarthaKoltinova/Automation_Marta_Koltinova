package Lesson5;

public class FData {
    public String roomWidth;
    public String roomHeight;
    public String lamWidth;
    public String lamHeight;
    public String inpack;
    public String pr;
    public String direction;
    public String offset;
    public String retreat;

    public FData(String roomWidth, String roomHeight, String lamWidth, String lamHeight, String inpack, String pr, String direction, String offset, String retreat) {
        this.roomWidth = roomWidth;
        this.roomHeight = roomHeight;
        this.lamWidth = lamWidth;
        this.lamHeight = lamHeight;
        this.inpack = inpack;
        this.pr = pr;
        this.direction = direction;
        this.offset = offset;
        this.retreat = retreat;
    }
}
